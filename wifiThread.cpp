WiFiInterface *wifi = 0;

static void wifiInit(void) {
    wifi = WiFiInterface::get_default_instance();
    printf("Connecting: %08X\n", (uint32)wifi);
    ThisThread::sleep_for(1000);
    for (;;) {
        nsapi_error_t errorCode = wifi->connect(WIFI_SSID, WIFI_PASSWORD, NSAPI_SECURITY_WPA_WPA2);
        if (errorCode == NSAPI_ERROR_OK) break;
        printf("ERROR=%08X\n", errorCode);
        ThisThread::sleep_for(2000); // If for some reason it doesnt work wait 2s and try again
    }
    printf("Connected\n");
}

void dump_response(HttpResponse* res) {
    printf("Status: %d - %s\n", res->get_status_code(), res->get_status_message().c_str());
 
    printf("Headers:\n");
    for (size_t ix = 0; ix < res->get_headers_length(); ix++) {
        printf("\t%s: %s\n", res->get_headers_fields()[ix]->c_str(), res->get_headers_values()[ix]->c_str());
    }
    printf("\nBody (%ud bytes):\n\n%s\n", res->get_body_length(), res->get_body_as_string().c_str());
}

void wifiTask(void) {
    wifiInit();

    for (;;) {
        ThisThread::sleep_for(30000);
        char url[128];
        sprintf(url, "%s?ID=%s&PRESSURE=%.2f", SERVER_CGI, SENSOR_ID, 999.99);
        HttpsRequest* get_req = new HttpsRequest(wifi, SSL_CA_PEM, HTTP_GET, url);
 
        HttpResponse* get_res = get_req->send();
        if (get_res) {
            printf("\n----- HTTPS GET response -----\n");
            dump_response(get_res);
        } else {
            printf("HttpRequest failed (error code %d)\n", get_req->get_error());
        }
        delete get_req;

    }
}
